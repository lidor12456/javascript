let grade = prompt("Please enter your grade");

if (grade >=0 && grade <=64) {
    console.log("F");
}
if (grade >=65 && grade <=69) {
    console.log("D");
}
if (grade >=70 && grade <=79) {
    console.log("C");
}
if (grade >=80 && grade <=89) {
    console.log("B");
}
if (grade >=90 && grade <=100) {
    console.log("A");
}
